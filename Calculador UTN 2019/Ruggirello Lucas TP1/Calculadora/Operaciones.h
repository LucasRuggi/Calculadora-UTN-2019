
/** \brief Suma los dos numeros que ingresa el usuario (A+B)
 *
 * \param int El primer numero que ingresa el usuario
 * \param int Segundo numero que ingresa el usuario
 * \return int Devuelve la suma de los dos numeros que ingreso el usuario(A+B)
 *
 */
float suma(float,float);

/** \brief Resta los dos numeros que ingresa el usuario(A-B)
 *
 * \param int El primer numero que ingresa el usuario
 * \param int El segundo numero que ingresa el usuario
 * \return int Devuelve la resta de los dos numeros que ingreso el usuario(A-B)
 *
 */
float resta(float,float);

/** \brief Multiplica los dos numeros que ingresa el usuario(A*B)
 *
 * \param int El primer numero que ingresa el usuario
 * \param int El segundo numero que ingresa el usuario
 * \return int Devuelve la multiplicacion de los dos numeros que ingreso el usuario(A*B)
 *
 */
float multiplicacion(float,float);

/** \brief Divide los dos numeros que ingresa el usuario(A/B)
 *
 * \param int El primer numero que ingresa el usuario
 * \param int El segundo numero que ingresa el usuario
 * \return float Devuelve la divicion de los dos numeros que ingresa el usuario(A/B)
 *
 */
float divicion(float,float);
/** \brief Saca el factor del numero ingresado
 *
 * \param numero int Se ingresa el numero que se quiere sacar el factor
 * \return int Devuelvo el factor del numero ingresado
 *
 */
long long int factorial(int numero);
